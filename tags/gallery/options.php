<p>This tag let to create a gallery from a set of images loaded in your blog web space.
The gallery use light box to show the real size images.</p>

<p><code>[t:gallery folder="folder with images" height="thumb height"]</code></p>

<p>In the attribute "folder" specify the name of directory containing .jpg images. 
The name is relative to the blog directory. So if you have a folder with anime images in 
"wp-content/galleries/anime" this is the right folder name to use.</p>
